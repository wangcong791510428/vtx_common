-module(pattern).
-compile(export_all).

get(standard_time) ->
"^(?<Year>([0-9]{4}))-(?<Month>([0-9]{2}))-(?<Day>([0-9]{2}))[T|t](?<Hour>([0-9]{2})):(?<Minute>([0-9]{2})):(?<Second>([0-9]{2}))([\.0-9]{4})?((?<TimeZone>[-|+][0-9:]*))$";

get(mark_time) ->
"^(?<Date>([0-9\-]*))\s(?<Time>([0-9\:]*))$";

get(hex) ->
"(^([0-9a-fA-F]*)$)";

get(uuid) ->
"(^([0-9a-fA-F]{32})$)";

get(email) ->
    "^(
            (\"[^\"\\f\\n\\r\\t\\v\\b]+\")
        |   ([\\w\\!\\#\\$\\%\\&\'\\*\\+\\-\\~\\/\\^\\`\\|\\{\\}]+
                (\\.[\\w\\!\\#\\$\\%\\&\\'\\*\\+\\-\\~\\/\^\`\\|\\{\\}]+)*
            )
    )
    @
    (
        (
            ([A-Za-z0-9\\-])+\\.
        )+
        [A-Za-z\\-]{2,}
    )";
get(url) ->
"(https?|ftp)://(-\.)?([^\s/?\.#-]+\.?)+(/[^\s]*)?";
get(domain) ->
"^[a-zA-Z][a-zA-Z0-9_]{0,31}";
get(range) ->
"^bytes=(?<Start>([0-9]+))-(?<End>([0-9]*))([\/0-9]*)?";
get(version) ->
".*";
get(_) ->
".*".

